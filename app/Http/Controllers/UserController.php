    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\User  $user
     * @return \Illuminate\Http\Response
     */
    public function destroy(User $user)
    {
        if (Gate::allows('isAdmin')) {
            $arr = [];
            foreach ($user->roles as $role) {
                $arr = $role->role;
            }
            if ($arr === 'admin') {
                return back()->with('delete_error', 'You cannot delete a user with ADMIN role!');
            }else{
                $user->delete();
                return back();
            }
        }else{
            return back();
        }
    }
